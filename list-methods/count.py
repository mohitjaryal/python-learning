# count() method in a list is used to find how many times a specific value appears in the list.

li = [1,2,4,2,4,2,5,5,2,1,7,8,9,6]

# count of 1
print('Count of 1 :',li.count(1))

# count of 2
print('Count of 2 :',li.count(2))

# count of 4
print('Count of 4 :',li.count(4))

# count of 5
print('Count of 5 :',li.count(5))